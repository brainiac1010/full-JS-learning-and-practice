
// The main difference between a for loop and a while loop is the syntax and structure.

// For Loop:

// The for loop is typically used when you know the number of iterations in advance.
// It consists of three parts: initialization, condition, and iteration.
Syntax:
for (initialization; condition; iteration) {
    // code block to be executed
}
Example:

for (var i = 0; i < 10; i++) {
    console.log(i);
}
// While Loop:

// The while loop is used when you want to execute a block of code repeatedly as long as a specified condition is true.
// It consists of a single condition.
Syntax:

while (condition) {
    // code block to be executed
}
Example:
var i = 0;
while (i < 10) {
    console.log(i);
    i++;
}