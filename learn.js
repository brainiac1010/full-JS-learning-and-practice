function ticketPrice(ticketNumber) {
    const first100ticket = 100;
    const second100ticket = 90;
    const restticket = 70;

    if (ticketNumber <= 100) {
        const first100ticketprice = first100ticket * ticketNumber;
        return first100ticketprice;
    } else if (ticketNumber <= 200) {
        const first100price = first100ticket * 100;
        const restquantityticket = ticketNumber - 100;
        const second100ticketPrice = first100price + restquantityticket * second100ticket;
        return second100ticketPrice;
    } else {
        const first100price = first100ticket * 100;
        const second100price = second100ticket * 100;
        const restticketquantity = ticketNumber - 200;
        const restticketPrice = restticketquantity * restticket;
        const totalticketPrice = first100price + second100price + restticketPrice;
        return totalticketPrice;
    }
}


